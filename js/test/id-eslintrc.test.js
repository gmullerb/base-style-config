const config = require('../config/configs/id-eslintrc')

const idRegex = new RegExp(config.rules['id-match'][1])

it('should allow valid id', () => {
  expect(idRegex.test('$$')).toBe(true)
  expect(idRegex.test('$')).toBe(true)
  expect(idRegex.test('_id')).toBe(true)
  expect(idRegex.test('to')).toBe(true)
  expect(idRegex.test('TA')).toBe(true)
  expect(idRegex.test('_$AS1W')).toBe(true)
  expect(idRegex.test('_$AS1W$')).toBe(true)
  expect(idRegex.test('_$A$AS1W')).toBe(true)
  expect(idRegex.test('_$A$AS1W1234567890123456789')).toBe(true)
  expect(idRegex.test('_$A$AS1W123456789012345678901234567890')).toBe(true)
  expect(idRegex.test('A_S')).toBe(true)
  expect(idRegex.test('A_S$C')).toBe(true)
  expect(idRegex.test('A_S$C1234567890123456789A')).toBe(true)
  expect(idRegex.test('A_S$C123456789012345678901234567890A')).toBe(true)
  expect(idRegex.test('_aA9bZ')).toBe(true)
  expect(idRegex.test('_aA9bZa1b2n3m4x5r6t7y8i9r')).toBe(true)
  expect(idRegex.test('_aA9bZa1b2n3m4x5r6t7y8i9r0w1q2s3e4g5j6k7l8n90')).toBe(true)
  expect(idRegex.test('AzB')).toBe(true)
  expect(idRegex.test('AzBa')).toBe(true)
  expect(idRegex.test('AzBaAa')).toBe(true)
  expect(idRegex.test('AzBaAa3m4x5r6t7y8i9r0w1q2')).toBe(true)
  expect(idRegex.test('AzBaAa3m4x5r6t7y8i9r0w1q2s3e4g5j6k7l8n903m4x5r6t7y8i9r0w1q2s3e4g5j6k7l8n90')).toBe(true)
  expect(idRegex.test('Azc')).toBe(true)
  expect(idRegex.test('AzcA')).toBe(true)
  expect(idRegex.test('AzaAa')).toBe(true)
  expect(idRegex.test('AzaAa3m4x5r6t7y8i9r0w1q2')).toBe(true)
  expect(idRegex.test('AzaAa3m4x5r6t7y8i9r0w1q2s3e4g5j6k7l8n903m4x5r6t7y8i9r0w1q2s3e4g5j6k7l8n90')).toBe(true)
})

it('should not allow invalid id', () => {
  expect(idRegex.test('_aA9BZ')).toBe(false)
  expect(idRegex.test('AzBaAa3m4x5r6t7y8i9r0w1q2_')).toBe(false)
})
